package main

import "fmt"

func main() {
	// Strings
	fmt.Println("Hello" + ", " + "World!")

	// Integers
	fmt.Println("10 * 2 =", 10*2)
	
	// Floats
	fmt.Println("10 / 3 =", 10/3)
	
	// Booleans
	fmt.Println("false && true =", false && true)
	fmt.Println("false || true =", false || true)
	fmt.Println("!false =", !false)

}

